package fredericosabino.fenixist.fenixdata;

public class CourseInfo {
	private String acronym;
	private String name;
	private String academicTerm;
	private String evaluationMethod;
	private long numberOfAttendingStudents;
	private String announcementLink;
	private String summaryLink;
	private String url;
	public String getAcronym() {
		return acronym;
	}
	public String getName() {
		return name;
	}
	public String getAcademicTerm() {
		return academicTerm;
	}
	public String getEvaluationMethod() {
		return evaluationMethod;
	}
	public long getNumberOfAttendingStudents() {
		return numberOfAttendingStudents;
	}
	public String getAnnouncementLink() {
		return announcementLink;
	}
	public String getSummaryLink() {
		return summaryLink;
	}
	public String getUrl() {
		return url;
	}
	
	
}
